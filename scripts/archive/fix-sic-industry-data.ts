async function fixSicIndustryData() {
  console.log('\n🔍 Scanning for SIC-style / incorrect sector+industry data...\n');

  const all = await prisma.ticker.findMany({
    select: { symbol: true, sector: true, industry: true },
    orderBy: { symbol: 'asc' },
  });

  console.log(`📊 Total tickers in DB: ${all.length}`);

  // Find problematic ones
  const problematic = all.filter(t => needsFix(t.sector, t.industry));
  console.log(`⚠️  Tickers needing fix: ${problematic.length}\n`);

  if (problematic.length === 0) {
    console.log('✅ All tickers look clean!');
    return;
  }

  let fixed = 0;
  let skipped = 0;
  const stillBad: string[] = [];

  for (const ticker of problematic) {
    const mapping = CORRECT_MAPPINGS[ticker.symbol];
    if (mapping) {
      await prisma.ticker.update({
        where: { symbol: ticker.symbol },
        data: { sector: mapping.sector, industry: mapping.industry, updatedAt: new Date() },
      });
      console.log(`  ✅ ${ticker.symbol.padEnd(8)} ${(ticker.sector || 'null').padEnd(30)} → ${mapping.sector} / ${mapping.industry}`);
      fixed++;
    } else {
      console.log(`  ⚠️  ${ticker.symbol.padEnd(8)} No mapping found (${ticker.sector || 'null'} / ${ticker.industry || 'null'})`);
      stillBad.push(ticker.symbol);
      skipped++;
    }
  }

  console.log('\n' + '═'.repeat(60));
  console.log(`✅ Fixed:   ${fixed}`);
  console.log(`⚠️  Skipped: ${skipped} (no mapping available)`);
  if (stillBad.length > 0) {
    console.log(`\n📋 Still needs manual mapping:\n   ${stillBad.join(', ')}`);
  }
  console.log('═'.repeat(60) + '\n');
}

