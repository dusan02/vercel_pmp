// ─────────────────────────────────────────────────────────────
// DETECT SIC-STYLE (ALL CAPS) VALUES
// ─────────────────────────────────────────────────────────────
function isSicStyle(value: string | null): boolean {
  if (!value) return false;
  // ALL CAPS with spaces/punctuation, or sector === industry pattern
  const trimmed = value.trim();
  if (trimmed.length < 2) return false;
  // Check if more than 60% uppercase letters (SIC codes are fully uppercase)
  const letters = trimmed.replace(/[^a-zA-Z]/g, '');
  if (letters.length === 0) return false;
  const upperCount = (trimmed.match(/[A-Z]/g) || []).length;
  const ratio = upperCount / letters.length;
  return ratio > 0.7 && trimmed === trimmed.toUpperCase();
}

function needsFix(sector: string | null, industry: string | null): boolean {
  if (!sector || sector === 'Unknown' || sector === 'N/A') return true;
  if (!industry || industry === 'Unknown' || industry === 'N/A') return true;
  if (sector === industry) return true;
  if (isSicStyle(sector)) return true;
  if (isSicStyle(industry)) return true;
  return false;
}

